"""Part 3 — Cultural Bias Score (driver + aggregation).

The question Part 3 answers: when the guide describes an artwork's historical
context, does its cultural attribution match what the work actually carries — or
does it flatten the work toward a dominant (eurocentric) perspective, omitting
the Byzantine / Islamic / Iberian / Flemish influences that are really there?
And does that bias differ BY LANGUAGE (the headline)?

Per-item pipeline (per sampled artwork x language):
    1. render the fixed cultural-context question in the target language
    2. (retrieval mode) retrieve(question) -> graph_context; (none mode) -> None
    3. answer(question, graph_context, language)  -> the guide's description
    4. cbs.classify(answer)  -> P(c), a distribution over CBS_LABELS  [TITLE-BLIND]
    5. CBS = D_KL(Q||P) vs the hand-curated Q(c) for that artwork (+ JSD, gaps)

Same sampled artworks asked in en/es/ca so per-language CBS is comparable.

Context mode (config.CBS_CONTEXT_MODE / --context):
  * 'retrieval' (default) = the real shipped product; a PRODUCT metric that mixes
    model bias with the graph's uneven coverage.
  * 'none' = answer with no graph context; the ABLATION isolating the model's own
    parametric bias. Use it to tell "the model is biased" from "the graph is thin".

Determinism: the classifier is pinned temperature=0 (+seed), but the answer it
scores is generated by an LLM with no exposed temperature, so per-language CBS is
a statistical estimate over the sample, not a per-artwork assertion.

Run:
    python -m eval.part3_cultural_bias                 # full run, all languages
    python -m eval.part3_cultural_bias --smoke          # tiny wiring check
    python -m eval.part3_cultural_bias --lang en        # English only
    python -m eval.part3_cultural_bias --context none   # pure-LLM ablation
    python -m eval.part3_cultural_bias --dry-run         # assemble + validate, no API
"""
