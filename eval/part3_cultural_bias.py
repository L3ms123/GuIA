# --- Question assembly ------------------------------------------------------
def build_artwork_set(per_origin: int, rng: random.Random) -> list[cg.CuratedItem]:
    """Sample curated artworks balanced across artist_origin, both themes present.

    Within each origin bucket we sample ``per_origin`` artworks, preferring a mix
    of religious/secular so neither theme is shut out of a bucket. Deterministic
    under a fixed seed (the curated list order is stable).
    """
    items = cg.load_items()
    by_origin: dict[str, list[cg.CuratedItem]] = defaultdict(list)
    for it in items:
        by_origin[it.artist_origin].append(it)

    sampled: list[cg.CuratedItem] = []
    for origin in sorted(by_origin):
        pool = list(by_origin[origin])
        rng.shuffle(pool)
        # Prefer theme diversity: interleave religious/secular within the bucket.
        relig = [it for it in pool if it.theme == "religious"]
        secul = [it for it in pool if it.theme == "secular"]
        ordered: list[cg.CuratedItem] = []
        while (relig or secul) and len(ordered) < per_origin:
            if secul:
                ordered.append(secul.pop())
            if len(ordered) < per_origin and relig:
                ordered.append(relig.pop())
        sampled.extend(ordered[:per_origin])
    return sampled


# --- Per-item execution -----------------------------------------------------
def run_item(item: cg.CuratedItem, lang: str, context_mode: str) -> dict[str, Any]:
    """Run one (artwork, language): retrieve? -> answer -> classify -> score.

    Never raises; each stage is wrapped so a failure is recorded with its
    ``error_stage`` and the run continues (Part 2 discipline).
    """
    from . import llm_bridge  # lazy import so --dry-run needs no Cohere key

    question = config.CBS_QUESTIONS.get(lang, config.CBS_QUESTIONS["en"]).format(title=item.title)
    sid = f"eval_p3_{uuid.uuid4().hex}"

    record: dict[str, Any] = {
        "id": item.title,
        "title": item.title,
        "language": lang,
        "theme": item.theme,
        "artist_origin": item.artist_origin,
        "confidence": item.confidence,
        "context_mode": context_mode,
        "question": question,
        "Q": item.distribution,
        "retrieval_class": None,
        "cypher": None,
        "n_rows": 0,
        "answer": None,
        "error": None,
        "error_stage": None,
    }

    # 1/2. Retrieve (optional).
    graph_context: Optional[dict] = None
    if context_mode == "retrieval":
        try:
            result = llm_bridge.retrieve(question, sid)
        except Exception as exc:  # noqa: BLE001
            record["error"] = f"{type(exc).__name__}: {exc}"
            record["error_stage"] = "retrieve"
            record["retrieval_class"] = "error"
            return record
        graph_context = result
        record["retrieval_class"] = llm_bridge.classify_retrieval(result)
        record["cypher"] = (result or {}).get("cypher")
        record["n_rows"] = len((result or {}).get("rows") or [])

    # 3. Answer.
    try:
        ans = llm_bridge.answer(question, sid, graph_context=graph_context, language=lang)
    except Exception as exc:  # noqa: BLE001
        record["error"] = f"{type(exc).__name__}: {exc}"
        record["error_stage"] = "answer"
        return record
    record["answer"] = ans

    # 4. Classify (title-blind).
    try:
        cls = cbs.classify(ans)
    except Exception as exc:  # noqa: BLE001
        record["error"] = f"{type(exc).__name__}: {exc}"
        record["error_stage"] = "classify"
        return record
    record.update(cls.to_record())
    if cls.parse_failed or cls.distribution is None:
        # Recorded + counted, excluded from CBS means (no usable P).
        record["error_stage"] = record["error_stage"] or "classify"
        return record

    # 5. Score.
    try:
        result = cbs.cbs_score(cls.distribution, item.distribution)
    except Exception as exc:  # noqa: BLE001
        record["error"] = f"{type(exc).__name__}: {exc}"
        record["error_stage"] = "score"
        return record
    record.update({
        "cbs": round(result.cbs, 6),
        "jsd": round(result.jsd, 6),
        "P": result.p_smoothed,
        "Q_smoothed": result.q_smoothed,
        "contributions": result.contributions,
        "gap": result.gap,
        "dominant_label": result.dominant_label(),
    })
    return record


# --- Aggregation ------------------------------------------------------------
def _scored(records: list[dict[str, Any]]) -> list[dict[str, Any]]:
    """Records with a usable CBS (classifier produced P and scoring succeeded)."""
    return [r for r in records if isinstance(r.get("cbs"), (int, float))]


def _mean(values: list[float]) -> Optional[float]:
    return round(statistics.fmean(values), 4) if values else None


def _rate(num: int, den: int) -> Optional[float]:
    return round(num / den, 4) if den else None


def _fmt(value: Optional[float]) -> str:
    return "—" if value is None else f"{value:.3f}"


def _metrics_block(records: list[dict[str, Any]]) -> dict[str, Any]:
    """Full metric set over an arbitrary record subset (reused for overall / per
    language / per origin / per theme) so numbers are computed identically."""
    scored = _scored(records)
    cbs_all = [r["cbs"] for r in scored]
    cov_ok = [r for r in scored if not r.get("low_coverage")]
    cbs_ok_cov = [r["cbs"] for r in cov_ok]
    jsd_all = [r["jsd"] for r in scored]

    # Mean per-class attribution gap P(c)-Q(c). Computed over NON-low-coverage
    # items only: a refusal / content-free answer has no cultural attribution to
    # measure a gap on, and smoothing would otherwise dump all its mass into
    # "other_global", manufacturing a fake fingerprint. (mean_CBS still counts
    # those refusals — erasure IS a bias outcome — but the *direction* of the
    # over/under-attribution fingerprint must come from answers that attributed.)
    gap_means: dict[str, Optional[float]] = {}
    for label in config.CBS_LABELS:
        vals = [r["gap"][label] for r in cov_ok if isinstance(r.get("gap"), dict) and label in r["gap"]]
        gap_means[label] = round(statistics.fmean(vals), 4) if vals else None

    n = len(records)
    return {
        "n_items": n,
        "n_scored": len(scored),
        "mean_CBS": _mean(cbs_all),
        "median_CBS": round(statistics.median(cbs_all), 4) if cbs_all else None,
        "mean_CBS_excl_low_coverage": _mean(cbs_ok_cov),
        "mean_JSD": _mean(jsd_all),
        "low_coverage_rate": _rate(sum(1 for r in scored if r.get("low_coverage")), len(scored)),
        "critical_lens_rate": _rate(sum(1 for r in scored if r.get("critical_lens_present")), len(scored)),
        "classify_parse_failures": sum(1 for r in records if r.get("classify_parse_failed")),
        "item_errors": sum(1 for r in records if r.get("error")),
        "retrieval_empty_rate": _rate(sum(1 for r in records if r.get("retrieval_class") == "empty"), n),
        "retrieval_error_rate": _rate(sum(1 for r in records if r.get("retrieval_class") == "error"), n),
        "mean_gap": gap_means,
    }


def aggregate(records: list[dict[str, Any]], uncovered: list[str]) -> dict[str, Any]:
    out = dict(_metrics_block(records))

    def slices(key: str) -> dict[str, Any]:
        vals: list[str] = []
        for r in records:
            v = r.get(key)
            if v and v not in vals:
                vals.append(v)
        return {v: _metrics_block([r for r in records if r.get(key) == v]) for v in vals}

    out["by_language"] = slices("language")
    out["by_artist_origin"] = slices("artist_origin")
    out["by_theme"] = slices("theme")
    out["by_confidence"] = slices("confidence")
    out["uncovered_inventory_titles"] = len(uncovered)
    return out


# --- Output writers ---------------------------------------------------------
def _csv_row(r: dict[str, Any]) -> dict[str, Any]:
    row: dict[str, Any] = {
        "language": r.get("language"),
        "title": r.get("title"),
        "theme": r.get("theme"),
        "artist_origin": r.get("artist_origin"),
        "confidence": r.get("confidence"),
        "cbs": r.get("cbs"),
        "jsd": r.get("jsd"),
        "coverage": r.get("coverage"),
        "low_coverage": r.get("low_coverage"),
        "critical_lens": r.get("critical_lens_present"),
        "dominant_label": r.get("dominant_label"),
        "retrieval_class": r.get("retrieval_class"),
        "n_rows": r.get("n_rows"),
        "classify_parse_failed": r.get("classify_parse_failed"),
        "error": r.get("error"),
    }
    p = r.get("P") or {}
    q = r.get("Q") or {}
    for label in config.CBS_LABELS:
        row[f"P_{label}"] = round(p[label], 4) if label in p else None
        row[f"Q_{label}"] = q.get(label)
    answer = (r.get("answer") or "").replace("\n", " ")
    row["answer"] = answer[:500]
    return row


def write_outputs(records: list[dict[str, Any]], summary: dict[str, Any],
                  run_config: dict[str, Any], ts: str) -> dict[str, str]:
    config.RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    stem = config.RESULTS_DIR / f"part3_{ts}"

    json_path = f"{stem}.json"
    with open(json_path, "w", encoding="utf-8") as fh:
        json.dump({"config": run_config, "summary": summary, "items": records},
                  fh, ensure_ascii=False, indent=2)

    csv_path = f"{stem}.csv"
    fields = list(_csv_row(records[0]).keys()) if records else []
    with open(csv_path, "w", encoding="utf-8-sig", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=fields)
        writer.writeheader()
        for r in records:
            writer.writerow(_csv_row(r))

    txt_path = f"{stem}.summary.txt"
    with open(txt_path, "w", encoding="utf-8") as fh:
        fh.write(_render_summary(summary, run_config, records))

    return {"json": json_path, "csv": csv_path, "summary": txt_path}


def _render_summary(s: dict[str, Any], run_config: dict[str, Any], records: list[dict[str, Any]]) -> str:
    lines: list[str] = []
    w = lines.append
    langs = run_config.get("languages") or list(s.get("by_language", {}).keys())
    w("=" * 80)
    w("GuIA Part 3 — Cultural Bias Score (CBS, multilingual)")
    w("=" * 80)
    w(f"Run timestamp : {run_config.get('timestamp')}")
    w(f"Context mode  : {run_config.get('context_mode')}  "
      f"({'real product: retrieve+ground' if run_config.get('context_mode') == 'retrieval' else 'pure-LLM ablation: no graph context'})")
    w(f"Languages     : {', '.join(langs)}   ({run_config.get('n_artworks')} artworks x {len(langs)} langs)")
    w(f"Items         : {s['n_items']}  (scored: {s['n_scored']}, "
      f"classify failures: {s['classify_parse_failures']}, item errors: {s['item_errors']})")
    w(f"CBS settings  : direction={run_config.get('cbs_direction')}  epsilon={run_config.get('cbs_epsilon')}  "
      f"(CBS magnitudes compare ONLY at fixed epsilon)")
    w("")
    w("WHAT THIS MEASURES: CBS = D_KL(Q||P), how far the guide's cultural")
    w("attribution P diverges from the curated TRUTH Q. High CBS = the answer")
    w("omits/flattens real influences. Lead with JSD (bounded) for ranking; CBS")
    w("is the directional headline. CBS is an optimistic LOWER bound (classifier")
    w("shares the guide's model family). Q is hand-curated art-historical judgment.")
    w("")
    w("-" * 80)
    w("HEADLINE — overall (all languages pooled)")
    w("-" * 80)
    w(f"  mean_CBS                    : {_fmt(s['mean_CBS'])}   (lower = better; D_KL(Q||P) in nats)")
    w(f"  median_CBS                  : {_fmt(s['median_CBS'])}")
    w(f"  mean_CBS_excl_low_coverage  : {_fmt(s['mean_CBS_excl_low_coverage'])}   (drops near-empty/evasive answers)")
    w(f"  mean_JSD                    : {_fmt(s['mean_JSD'])}   (bounded by ln2 ≈ 0.693; symmetric)")
    w(f"  low_coverage_rate           : {_fmt(s['low_coverage_rate'])}   (answers with little/no cultural framing)")
    w(f"  critical_lens_rate          : {_fmt(s['critical_lens_rate'])}   (answers engaging gender/power/colonial lens)")
    if run_config.get("context_mode") == "retrieval":
        w(f"  retrieval empty / error     : {_fmt(s['retrieval_empty_rate'])} / {_fmt(s['retrieval_error_rate'])}")
    w("")
    w("-" * 80)
    w("CBS BY LANGUAGE  (same artworks asked in each language — THE headline)")
    w("-" * 80)
    hdr = f"  {'lang':<6}{'n':>4}{'meanCBS':>9}{'medCBS':>8}{'exclLowC':>9}{'meanJSD':>9}{'lowCov':>8}"
    w(hdr)
    w("  " + "-" * (len(hdr) - 2))
    for lang in langs:
        b = s["by_language"].get(lang)
        if not b:
            continue
        w(f"  {lang:<6}{b['n_scored']:>4}{_fmt(b['mean_CBS']):>9}{_fmt(b['median_CBS']):>8}"
          f"{_fmt(b['mean_CBS_excl_low_coverage']):>9}{_fmt(b['mean_JSD']):>9}{_fmt(b['low_coverage_rate']):>8}")
    w("  NOTE: where lowCov is high, raw meanCBS is dominated by REFUSALS (empty")
    w("  retrieval -> 'the graph lacks info' -> coverage 0 -> high CBS), NOT by")
    w("  cultural bias. Read exclLowC as the bias signal there; raw meanCBS as a")
    w("  product-coverage signal. A per-language gap usually tracks retrieval luck.")
    w("")
    w("-" * 80)
    w("CBS BY ARTIST ORIGIN  (does it flatten non-Italian works hardest?)")
    w("-" * 80)
    for origin, b in s["by_artist_origin"].items():
        w(f"  {origin:<14} n={b['n_scored']:<3} mean_CBS={_fmt(b['mean_CBS'])} "
          f"mean_JSD={_fmt(b['mean_JSD'])} lowCov={_fmt(b['low_coverage_rate'])}")
    w("")
    w("-" * 80)
    w("CBS BY THEME / CONFIDENCE")
    w("-" * 80)
    for theme, b in s["by_theme"].items():
        w(f"  theme={theme:<10} n={b['n_scored']:<3} mean_CBS={_fmt(b['mean_CBS'])} mean_JSD={_fmt(b['mean_JSD'])}")
    for conf, b in s["by_confidence"].items():
        w(f"  conf={conf:<10}  n={b['n_scored']:<3} mean_CBS={_fmt(b['mean_CBS'])}  "
          f"(low-confidence Q rows are softer evidence)")
    w("")
    w("-" * 80)
    w("MEAN ATTRIBUTION GAP  P(c) − Q(c) per class  (the eurocentric fingerprint)")
    w("-" * 80)
    w("  positive = guide OVER-attributes this culture; negative = UNDER-attributes (omits)")
    w("  (computed over answers WITH cultural content only — refusals excluded, so")
    w("   an other_global spike here is a real attribution choice, not refusal mass)")
    gap = s.get("mean_gap") or {}
    for label in config.CBS_LABELS:
        v = gap.get(label)
        bar = ""
        if isinstance(v, (int, float)):
            n = int(min(20, abs(v) * 40))
            bar = ("+" if v >= 0 else "-") * n
        w(f"  {label:<22} {(_fmt(v) if v is not None else '—'):>7}  {bar}")
    w("")
    w("-" * 80)
    w("WORST OFFENDERS — highest CBS (what the answer got culturally wrong)")
    w("-" * 80)
    worst = sorted(_scored(records), key=lambda r: r["cbs"], reverse=True)
    if not worst:
        w("  (none scored)")
    for r in worst[:15]:
        dom = r.get("dominant_label")
        q_dom = (r.get("Q") or {}).get(dom)
        p_dom = (r.get("P") or {}).get(dom)
        flag = " [LOW COVERAGE]" if r.get("low_coverage") else ""
        w(f"  [{r.get('language')}|{r.get('artist_origin')}] {r.get('title')}  CBS={r['cbs']:.3f} JSD={r['jsd']:.3f}{flag}")
        if dom is not None:
            w(f"      driven by '{dom}': Q={q_dom if q_dom is not None else '?'} "
              f"P={f'{p_dom:.2f}' if isinstance(p_dom, (int, float)) else '?'}")
        snippet = (r.get("answer") or "").replace("\n", " ").strip()
        if snippet:
            w(f"      answer: {snippet[:160]}{'...' if len(snippet) > 160 else ''}")
    if len(worst) > 15:
        w(f"  ... and {len(worst) - 15} more (see CSV, sort by cbs desc)")
    w("=" * 80)
    return "\n".join(lines) + "\n"


# --- Entry point ------------------------------------------------------------
def main(argv: Optional[list[str]] = None) -> int:
    parser = argparse.ArgumentParser(description="Part 3 — Cultural Bias Score (multilingual).")
    parser.add_argument("--smoke", action="store_true", help="tiny run (1/origin/language) to check wiring")
    parser.add_argument("--per-origin", type=int, default=None,
                        help=f"artworks sampled per artist-origin (default {config.CBS_PER_ORIGIN})")
    parser.add_argument("--seed", type=int, default=config.RANDOM_SEED, help="sampling seed")
    parser.add_argument("--lang", default=None,
                        help=f"comma-separated languages (default: {','.join(config.LANGUAGES)})")
    parser.add_argument("--context", choices=("retrieval", "none"), default=None,
                        help=f"context mode (default {config.CBS_CONTEXT_MODE})")
    parser.add_argument("--dry-run", action="store_true",
                        help="validate the curated table, assemble & print questions+Q, make NO Cohere calls")
    args = parser.parse_args(argv)

    if args.lang:
        languages = [c.strip() for c in args.lang.split(",") if c.strip()]
        unknown = [c for c in languages if c not in config.LANGUAGE_NAMES]
        if unknown:
            print(f"[eval] Unknown language code(s): {unknown}. Known: {list(config.LANGUAGE_NAMES)}", file=sys.stderr)
            return 2
    else:
        languages = list(config.LANGUAGES)

    per_origin = config.SMOKE_CBS_PER_ORIGIN if args.smoke else (
        args.per_origin if args.per_origin is not None else config.CBS_PER_ORIGIN)
    context_mode = args.context if args.context is not None else config.CBS_CONTEXT_MODE

    # Validate the curated table FIRST — fail fast before any API spend.
    report = cg.load_and_validate()
    if not report.ok:
        print("[eval] ERROR: the curated cultural ground-truth table is invalid. "
              "Run `python -m eval.cultural_groundtruth --validate` to see why.", file=sys.stderr)
        for e in (report.errors + [f"unmatched: {t!r}" for t in report.unmatched_titles])[:10]:
            print(f"[eval]   - {e}", file=sys.stderr)
        return 2

    rng = random.Random(args.seed)
    artworks = build_artwork_set(per_origin, rng)
    if not artworks:
        print("[eval] ERROR: no artworks sampled — is the curated table empty?", file=sys.stderr)
        return 2

    if args.dry_run:
        print(f"[eval] Curated table OK ({len(report.items)} rows, "
              f"{report.uncovered_titles and len(report.uncovered_titles) or 0} inventory titles uncovered).")
        print(f"[eval] Assembled {len(artworks)} artworks x {len(languages)} languages "
              f"= {len(artworks) * len(languages)} items  (context={context_mode}; no API calls):\n")
        for i, it in enumerate(artworks, 1):
            top = sorted(it.distribution.items(), key=lambda kv: kv[1], reverse=True)
            mix = " ".join(f"{c}={v:.2f}" for c, v in top if v > 0)
            print(f"  {i:>2}. [{it.artist_origin}|{it.theme}|conf={it.confidence}] {it.title}")
            print(f"        Q: {mix}")
            for lang in languages:
                print(f"        {lang}: {config.CBS_QUESTIONS.get(lang, config.CBS_QUESTIONS['en']).format(title=it.title)}")
        print(f"\n  per-origin: {per_origin}   languages: {languages}   context: {context_mode}")
        return 0

    # Live run. Neo4j only matters in retrieval mode; warn but don't abort.
    preflight(require_neo4j=False, warn_neo4j=(context_mode == "retrieval"))

    from . import llm_bridge
    if not llm_bridge.BACKOFF_INSTALLED:
        print("[eval] NOTE: global Cohere backoff could not be installed; 429s will error "
              "the item rather than retry.", file=sys.stderr)
    if context_mode == "retrieval" and not llm_bridge.neo4j_is_configured():
        print("[eval] NOTE: Neo4j not configured — retrieval will error for every item; "
              "consider --context none for a pure-LLM run.", file=sys.stderr)

    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    work = [(it, lang) for lang in languages for it in artworks]
    run_config = {
        "timestamp": ts,
        "part": 3,
        "smoke": args.smoke,
        "seed": args.seed,
        "per_origin": per_origin,
        "context_mode": context_mode,
        "languages": languages,
        "n_artworks": len(artworks),
        "n_items": len(work),
        "classifier_model": config.CLASSIFIER_MODEL,
        "cbs_direction": config.CBS_DIRECTION,
        "cbs_epsilon": config.CBS_EPSILON,
        "cbs_coverage_min": config.CBS_COVERAGE_MIN,
        "labels": config.CBS_LABELS,
        "request_sleep_s": config.REQUEST_SLEEP_S,
        "global_backoff_installed": llm_bridge.BACKOFF_INSTALLED,
        "neo4j_configured": llm_bridge.neo4j_is_configured(),
        "curated_rows": len(report.items),
    }

    print(f"[eval] Part 3 starting: {len(artworks)} artworks x {len(languages)} langs = {len(work)} items, "
          f"context={context_mode}, sleep={config.REQUEST_SLEEP_S}s, "
          f"backoff={'all' if llm_bridge.BACKOFF_INSTALLED else 'none'}")
    records: list[dict[str, Any]] = []
    for i, (it, lang) in enumerate(work, 1):
        print(f"[eval] ({i}/{len(work)}) [{lang}|{it.artist_origin}] {it.title[:50]}", flush=True)
        try:
            record = run_item(it, lang, context_mode)
        except Exception as exc:  # noqa: BLE001 — absolute backstop; never abort the run
            record = {"id": it.title, "title": it.title, "language": lang,
                      "theme": it.theme, "artist_origin": it.artist_origin,
                      "confidence": it.confidence, "Q": it.distribution,
                      "error": f"UNCAUGHT {type(exc).__name__}: {exc}",
                      "error_stage": "run_item", "traceback": traceback.format_exc()}
        records.append(record)
        if record.get("error"):
            print(f"       ! error @ {record.get('error_stage')}: {record['error']}", file=sys.stderr)
        elif isinstance(record.get("cbs"), (int, float)):
            print(f"       -> CBS={record['cbs']:.3f} JSD={record['jsd']:.3f} "
                  f"coverage={record.get('coverage')} dom={record.get('dominant_label')}")
        else:
            print(f"       -> classify failed (parse); recorded, excluded from means")
        if i < len(work):
            time.sleep(config.REQUEST_SLEEP_S)

    summary = aggregate(records, report.uncovered_titles)
    paths = write_outputs(records, summary, run_config, ts)

    print("\n" + _render_summary(summary, run_config, records))
    print("[eval] Wrote:")
    for kind, path in paths.items():
        print(f"  {kind:<8}: {path}")
    return 0


